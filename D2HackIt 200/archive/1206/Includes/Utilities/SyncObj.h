#ifndef __SYNCOBJ_H__
#define __SYNCOBJ_H__

#include <windows.h>
#include <stdio.h>

class CSyncObj  
{
public:
	
	/////////////////////////////////////////////////////////////////
	// Constructor & Destructor
	/////////////////////////////////////////////////////////////////
	CSyncObj() { ::InitializeCriticalSection(&m_cs); }
	virtual ~CSyncObj() { ::DeleteCriticalSection(&m_cs); }
	
	/////////////////////////////////////////////////////////////////
	// Public Operations
	/////////////////////////////////////////////////////////////////
	void Lock() const { ::EnterCriticalSection((LPCRITICAL_SECTION)&m_cs); }
	void Unlock() const { ::LeaveCriticalSection((LPCRITICAL_SECTION)&m_cs); }	

private:

	/////////////////////////////////////////////////////////////////
	// Private Member Data
	/////////////////////////////////////////////////////////////////
	CRITICAL_SECTION m_cs;
};

#endif // __SYNCOBJ_H__
